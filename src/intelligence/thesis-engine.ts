import OpenAI from "openai";

export interface TradeThesis {

  asset: string;
  direction: "BUY" | "SELL" | "NO_TRADE";
  confidence: number;

narrative: string;
risk: string;
  tradePlan: {
    entry: string;
    stopLoss: string;
    targets: string[];
  };

}

export class ThesisEngine {

  private openai: OpenAI;

  constructor() {

    this.openai = new OpenAI({
      apiKey: process.env.OPENAI_API_KEY
    });

  }

  async buildThesis(input: {

    asset: string;
    direction: "BUY" | "SELL" | "NO_TRADE";
    confidence: number;

    technical: {
      trend: string;
      momentum: string;
      structure: string;
    };

    macroRegime: string;

    events: string[];

    entry?: number;
    entryHigh?: number;
    stopLoss?: number;
    targets?: number[];

  }): Promise<TradeThesis> {

   const prompt = `
You are the strategic analysis layer of an institutional trading system.

Your role is to interpret signals generated by deterministic engines.

IMPORTANT RULES

You must ONLY use the information provided below.
You must NOT invent or assume:

- ETF flows
- options positioning
- bond yields
- stablecoin demand
- liquidity injections
- derivatives positioning
- order book dynamics

If the system did not provide a signal, you must not reference it.

Your analysis must rely ONLY on:

• technical structure
• macro regime
• geopolitical events
• the provided trade levels

System Inputs

Asset: ${input.asset}
Direction: ${input.direction}
Confidence: ${input.confidence}%

Technical
Trend: ${input.technical.trend}
Momentum: ${input.technical.momentum}
Structure: ${input.technical.structure}

Macro Regime
${input.macroRegime}

Relevant Events
${input.events.join("\n")}

Trade Plan
Entry: ${input.entry} - ${input.entryHigh}
Stop Loss: ${input.stopLoss}
Targets: ${input.targets?.join(", ")}

Write the explanation using EXACTLY this structure:

Strategic View:
(2–3 sentences explaining the trade using only the provided signals)

Dominant Driver:
(the main factor supporting the trade)

Key Risk:
(the biggest risk given the macro or geopolitical environment)

Invalidation:
(the condition under which the trade fails)
`;

const response = await this.openai.chat.completions.create({

  model: process.env.OPENAI_MODEL || "gpt-5",

  messages: [
    {
      role: "system",
      content: "You are the strategic review layer of a quantitative macro trading system."
    },
    {
      role: "user",
      content: prompt
    }
  ]

});

    const narrative =
      response.choices[0].message?.content || "";

const riskMatch = narrative.match(/Key Risk:\s*([\s\S]*?)\n\nInvalidation:/);

const risk = riskMatch ? riskMatch[1].trim() : "";

return {

  asset: input.asset,
  direction: input.direction,
  confidence: input.confidence,

  narrative,
  risk,

  tradePlan: {
    entry: `${input.entry} - ${input.entryHigh}`,
    stopLoss: `${input.stopLoss}`,
    targets: input.targets?.map(t => String(t)) || []
  }

};

  }

}